package com.acc.lkm.JavaDay1;

public class BitwiseOperator 
{
public static void main(String[] args) 
{
	boolean x=true;
	boolean y=false;
	
	boolean a=x|y;
	boolean b=x^y;
	boolean c=x&y;
	boolean d=!x;
	
	System.out.println("x|y="+a);
	System.out.println("x^y="+b);
	System.out.println("x&y="+c);
	System.out.println("!x="+d);
	
}
}
